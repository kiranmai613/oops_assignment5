# Challenge 1: Square Numbers and Return Their Sum

class Point :
        
        
    def __init__(self):
        self.x=int(input("enter first number"))
        self.y=int(input("enter second number"))
        self.z=int(input("enter third number"))
        
    def sqSum(self):

        a=self.x**2+self.y**2+self.z**2
        return a
    
     
obj=Point()
print(obj.sqSum())

      
    
    
        
        
    
        
    


    


